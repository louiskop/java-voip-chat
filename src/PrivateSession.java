	/**
	 * This method is called when the user clicks on the "disconnect" label.
	 * It sends a packet to the server to disconnect the user from the session.
	 * 
	 * @param evt the MouseEvent that triggered the method
	 */
	private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jLabel6MouseClicked

		// use invite packet for isPrivate attr
		Packet packet = new Packet("disconnectSession", true, sessionId, null);

		try {
			out.writeObject(packet);
		} catch (IOException e) {
			e.printStackTrace();
		}

	}// GEN-LAST:event_jLabel6MouseClicked

