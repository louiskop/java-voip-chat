	/**
	 * This method is called when the user clicks on the "send message" label.
	 * It sends a message packet to the server with the contents of the jTextField1
	 * text field.
	 * If the message starts with the "/call" command, it sends a call packet
	 * instead.
	 * 
	 * @param evt the MouseEvent that triggered the method
	 */
	private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jLabel4MouseClicked
		String s = jTextField1.getText();
		Packet packet;

		// check for call command
		if (s.length() > 8) {
			if (s.substring(0, 9).equals("/calllist")) {
				// request the call list from server
				packet = new Packet("calllist", false, sessionId, null);
			} else {
				packet = new Packet("message", username, sessionId, s, true);
			}
		} else if (s.length() > 4) {

			if (s.substring(0, 5).equals("/call")) {

				int channel = Integer.parseInt(s.substring(6, 7));

				// join or leave the call
				packet = new Packet("call", sessionId, inCall, true, 0, new ArrayList<InetAddress>(), channel);
				inCall = !inCall;
			} else {
				packet = new Packet("message", username, sessionId, s, true);
			}
		} else {
			packet = new Packet("message", username, sessionId, s, true);
		}

		try {
			out.writeObject(packet);
		} catch (IOException e) {
			e.printStackTrace();
		}
		jTextField1.setText("");
	}// GEN-LAST:event_jLabel4MouseClicked

